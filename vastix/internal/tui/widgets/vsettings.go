// NewVSettingsWidget creates a new VSettings widget
func NewVSettingsWidget(db *database.Service) *VSettingsWidget {
	// No list headers needed since we don't have a list view
	listHeaders := []string{}

	// No form hints needed - we'll override GetInputs
	formHints := &common.FormHints{}

	// Create the base widget with no extra widgets
	baseWidget := NewBaseWidget(db, listHeaders, formHints, "vsettings", nil, nil)

	widget := &VSettingsWidget{
		BaseWidget: baseWidget,
	}

	// Set parent reference
	widget.SetParentForBaseWidget(widget, false)

	// Override the create callback to use custom logic
	widget.SetCreateCallback(widget.createVSettings)

	return widget
}

// GetAllowedNavigatorModes restricts this widget to Create and Details modes
func (v *VSettingsWidget) GetAllowedNavigatorModes() []common.NavigatorMode {
	return []common.NavigatorMode{
		common.NavigatorModeCreate,
		common.NavigatorModeDetails,
	}
}

// InitialMode sets the initial mode to Create (since we don't have a list)
func (v *VSettingsWidget) InitialMode() common.NavigatorMode {
	return common.NavigatorModeCreate
}

// SetMode overrides BaseWidget.SetMode to prevent form reset for VSettings
func (v *VSettingsWidget) SetMode(mode common.NavigatorMode) {

	// Only allow create mode for VSettings
	if mode != common.NavigatorModeCreate && mode != common.NavigatorModeDetails {
		v.log.Debug("VSettings only supports create and details modes, forcing create mode",
			zap.String("requested_mode", mode.String()))
		mode = common.NavigatorModeCreate
	}

	v.WidgetNavigator.SetMode(mode)

	// Initialize CreateAdapter inputs when switching to create mode
	if mode == common.NavigatorModeCreate {
		// Only fetch and set inputs if they haven't been set yet
		if len(v.CreateAdapter.GetInputs()) == 0 {
			inputs, err := v.GetInputs()
			if err != nil {
				v.log.Error("Failed to get inputs", zap.Error(err))
				return
			}
			v.CreateAdapter.SetInputs(inputs)
			v.log.Debug("VSettings inputs loaded from API (cache or fresh)")
		}
		// DO NOT call ResetCreateForm() - we want to keep the fetched values!
	}
}

// View overrides the base View to ensure we always show create form
func (v *VSettingsWidget) View() string {
	currentMode := v.GetMode()

	// Force create mode if somehow we're in list mode
	if currentMode == common.NavigatorModeList {
		v.SetModeMust(common.NavigatorModeCreate)
	}

	// Handle different modes
	switch v.GetMode() {
	case common.NavigatorModeCreate:
		return v.ViewCreateForm()
	case common.NavigatorModeDetails:
		return v.ViewDetails()
	default:
		// Fallback to create form
		return v.ViewCreateForm()
	}
}

// GetInputs dynamically generates input fields from current VSettings via API
// Uses caching to avoid multiple API calls
func (v *VSettingsWidget) GetInputs() (common.Inputs, error) {
	var inputs common.Inputs

	// Check if we have cached VSettings
	var vsettings core.Record

	if v.cacheValid && v.cachedVSettings != nil {
		v.log.Debug("Using cached VSettings",
			zap.Int("cached_count", len(v.cachedVSettings)))
		vsettings = v.cachedVSettings
	} else {
		v.log.Debug("Cache invalid or empty, fetching from API")

		// Get active REST client
		rest, err := getActiveRest(v.db)
		if err != nil {
			v.log.Warn("Failed to get active REST client, returning empty inputs", zap.Error(err))
			return inputs, err
		}

		// Fetch current VSettings from the API
		clusterID := "1" // Default cluster ID
		v.log.Debug("Fetching VSettings from API", zap.String("cluster_id", clusterID))

		vsettings, err = rest.Clusters.ClusterVsettings_GET(clusterID, nil)
		if err != nil {
			v.log.Warn("Failed to fetch VSettings, returning empty inputs", zap.Error(err))
			return inputs, err
		}

		delete(vsettings, "@resourceType")

		// Get default values
		defaults := getVSettingsDefaults()
		v.log.Debug("Loaded VSettings defaults",
			zap.Int("defaults_count", len(defaults)))

		// Merge defaults with API response (API values override defaults)
		// Start with all defaults
		merged := make(core.Record)
		for key, value := range defaults {
			merged[key] = value
		}
		// Override with API values
		for key, value := range vsettings {
			merged[key] = value
		}

		v.log.Debug("Merged VSettings with defaults",
			zap.Int("total_count", len(merged)),
			zap.Int("from_api", len(vsettings)),
			zap.Int("from_defaults", len(defaults)))

		// Cache the merged result
		v.cachedVSettings = merged
		v.cacheValid = true
		vsettings = merged
	}

	// Create inputs dynamically from the fetched vsettings
	// We need to ensure consistent ordering, so let's extract keys and sort them
	keys := make([]string, 0, len(vsettings))
	for key := range vsettings {
		keys = append(keys, key)
	}

	// Sort keys alphabetically for consistent display
	sortKeys(keys)

	// Create input fields for each vsetting
	for _, key := range keys {
		value := vsettings[key]
		valueStr := fmt.Sprintf("%v", value)

		// Check if value is "true" or "false" - create bool input
		if valueStr == "true" || valueStr == "false" {
			defaultValue := valueStr == "true"
			inputs.NewBoolInput(key, key, false, defaultValue)
			// Mark as touched so the value is visible in the UI
			if len(inputs) > 0 {
				inputs[len(inputs)-1].Touched = true
			}
		} else {
			// For all other values, create text input with current value as default
			inputs.NewTextInput(key, key, false, valueStr)
			// Mark as touched so the value is visible in the UI
			if len(inputs) > 0 {
				inputs[len(inputs)-1].Touched = true
			}
		}
	}

	v.log.Debug("Generated inputs from VSettings",
		zap.Int("input_count", len(inputs)))

	return inputs, nil
}

// clearCache invalidates the cached VSettings
func (v *VSettingsWidget) clearCache() {
	v.cacheValid = false
	v.cachedVSettings = nil
	v.log.Debug("VSettings cache cleared")
}

// sortKeys is a simple bubble sort for string slices
func sortKeys(keys []string) {
	n := len(keys)
	for i := 0; i < n-1; i++ {
		for j := 0; j < n-i-1; j++ {
			if keys[j] > keys[j+1] {
				keys[j], keys[j+1] = keys[j+1], keys[j]
			}
		}
	}
}

// createVSettings is the custom create callback that formats data and calls PATCH endpoint
func (v *VSettingsWidget) createVSettings(rest *vast_client.VMSRest) (tea.Msg, error) {
	// Get the inputs
	inputs := v.CreateAdapter.GetInputs()

	// Validate inputs
	if err := inputs.Validate(); err != nil {
		return msg_types.ErrorMsg{Err: err}, err
	}

	// Build vsettings map from inputs
	vsettingsMap := make(map[string]string)

	for _, input := range inputs {
		fieldName := input.GetLabel()

		// Only include inputs that have been modified from their initial value
		if !input.IsDirty() {
			continue
		}

		// Handle different input types
		var valueStr string
		if input.IsBoolInput() {
			boolValue := input.Bool()
			valueStr = fmt.Sprintf("%t", boolValue)
		} else {
			// For text inputs, get the string value
			valueStr = input.String()
			// Skip empty strings
			if valueStr == "" {
				continue
			}
		}

		// Add to vsettings map
		vsettingsMap[fieldName] = valueStr
	}

	if len(vsettingsMap) == 0 {
		err := fmt.Errorf("at least one vsetting must be modified")
		return msg_types.ErrorMsg{Err: err}, err
	}

	// Hardcoded cluster ID (always "1")
	clusterID := "1"

	v.log.Info("Updating VSettings",
		zap.String("cluster_id", clusterID),
		zap.Int("vsettings_count", len(vsettingsMap)),
		zap.Any("vsettings", vsettingsMap))

	// Build the payload in the required format
	payload := core.Params{
		"vsettings": vsettingsMap,
	}

	// Call the PATCH endpoint
	err := rest.Clusters.ClusterVsettings_PATCH(clusterID, payload)
	if err != nil {
		v.log.Error("Failed to update VSettings",
			zap.String("cluster_id", clusterID),
			zap.Error(err))
		return msg_types.ErrorMsg{Err: err}, err
	}

	v.log.Info("VSettings updated successfully",
		zap.String("cluster_id", clusterID))

	// Clear the cache so next time we fetch fresh data
	v.clearCache()

	// Fetch the updated VSettings to display in details view
	updatedVSettings, err := rest.Clusters.ClusterVsettings_GET(clusterID, nil)
	if err != nil {
		v.log.Error("Failed to fetch updated VSettings",
			zap.String("cluster_id", clusterID),
			zap.Error(err))
		return msg_types.ErrorMsg{Err: err}, err
	}

	v.log.Info("Fetched updated VSettings for display",
		zap.String("cluster_id", clusterID),
		zap.Int("vsettings_count", len(updatedVSettings)))

	// Set content and switch to details mode
	v.SetContent(updatedVSettings)
	v.SetModeMust(common.NavigatorModeDetails)

	return msg_types.SetDataMsg{
		UseSpinner: false,
	}, nil
}

